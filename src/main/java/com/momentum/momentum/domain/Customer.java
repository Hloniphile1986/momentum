package com.momentum.momentum.domain;

public class Customer {
    private String name;
    private int points;

    public Customer() {};

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }
}
